package ospringo.worldsimulator.loader;

public class Meta {

    private String name;
    private String describe;
    private String author;
    private String loader;

    private Meta() { }

    public String getName() {
        return name;
    }

    public String getDescribe() {
        return describe;
    }

    public String getAuthor() {
        return author;
    }

    public String getLoader() {
        return loader;
    }
}
